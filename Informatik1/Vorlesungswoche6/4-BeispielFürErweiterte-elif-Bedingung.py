i = int(input("Bitte Kennzahl eingeben: "))
if i == 1:
      print("Montag")
elif i == 2:
      print("Dienstag")
elif i == 3:
      print("Mittwoch")
elif i == 4:
      print("Donnerstag")
elif i == 5:
      print("Freitag")
elif i == 6:
      print("Samstag")
elif i == 7:
      print("Sonntag")
else:
      print("Falsche Tageskennzahl!")
