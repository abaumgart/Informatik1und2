
def main():
    # 1. Deklaration und Initialisierung der Variablen
    num1 = 10             # Ganzzahl
    num2 = 3.14           # Gleitkommazahl
    text = "Hello, World" # Zeichenkette

    # 2. Arithmetische Operationen
    print("Addition:", num1 + num2)
    print("Subtraktion:", num1 - num2)
    print("Multiplikation:", num1 * num2)
    print("Division:", num1 / num2)
    print("Ganzzahlige Division:", num1 // num2)
    print("Potenzierung:", num1 ** num2)
    print("Modulo:", num1 % num2)

    # 3. Boolesche Werte
    true_flag = True
    false_flag = False
    
    print("True Flag:", true_flag)
    print("False Flag:", false_flag)

    # 4. Zeichenketten-Verkettung
    new_text = text + " - Willkommen in Informatik 1!"
    print("Verkettete Zeichenkette:", new_text)

# Hauptprogramm ausf√ºhren

main()